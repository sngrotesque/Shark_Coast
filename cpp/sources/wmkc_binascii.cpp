wmkcByte wmkcBinascii::toTop(wmkcByte c)
{
    if((c >> 4) < 0x0a) {
        return (c >> 4) + 0x30;
    } else {
        return (c >> 4) + 0x57;
    }
}

wmkcByte wmkcBinascii::toBot(wmkcByte c)
{
    if((c & 0x0f) < 0x0a) {
        return (c & 0x0f) + 0x30;
    } else {
        return (c & 0x0f) + 0x57;
    }
}

