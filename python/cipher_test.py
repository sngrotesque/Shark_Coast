import random

def create_sbox():
    sbox = [_ for _ in range(256)]
    random.shuffle(sbox)
    return sbox

def create_rsbox(sbox :list):
    rsbox = [0 for _ in range(256)]
    for x in range(len(rsbox)):
        rsbox[sbox[x]] = x
    return rsbox

def sbox_replace(sbox :list, array :bytearray):
    for i in range(len(array)):
        array[i] = sbox[array[i]]

def pbox_permute(pbox :list, array :bytearray):
    temp = array.copy()
    for i in range(len(array)):
        array[i] = temp[pbox[i]]

def cipher(p :bytearray, sbox :list, pbox :list):
    for _ in range(16):
        sbox_replace(sbox, p)
        pbox_permute(pbox, p)
    return p

